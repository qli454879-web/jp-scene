def get_conn():
    return psycopg.connect(SUPABASE_DB_URL, prepare_threshold=None,
                           options="-c plan_cache_mode=force_custom_plan")


def update_ability(conn, cur, dry_run, word, reading, pos, freq, pitch, meaning, examples, insight, source, heatmap):
    """UPDATE 一条技能词条的所有字段"""
    cur.execute("""
        SELECT id FROM vocab_library
        WHERE word = %s AND tags @> ARRAY['gaming_valorant','gaming_val_agents']::text[]
    """, (word,))
    row = cur.fetchone()
    if not row:
        log.warning(f"  ⚠ 未找到: {word}")
        return False

    wid = row[0]
    if dry_run:
        log.info(f"  [DRY] UPDATE: {word}")
        return True

    cur.execute("""
        UPDATE vocab_library SET
            meaning = %s,
            pos = %s,
            frequency = %s,
            pitch = %s,
            examples = %s::jsonb,
            insight_text = %s,
            social_context = %s::jsonb,
            heatmap_data = %s::jsonb,
            source = %s,
            is_ai_enriched = TRUE
        WHERE id = %s
    """, (meaning, pos, freq, pitch,
          json.dumps(examples, ensure_ascii=False),
          insight,
          json.dumps(SOCIAL, ensure_ascii=False),
          json.dumps(heatmap, ensure_ascii=False),
          source, wid))
    return True


async def gen_mp3(sem, wid, word, reading):
    """生成 MP3 并上传到 Supabase Storage"""
    async with sem:
        try:
            romaji = reading or word
            safe = re.sub(r'[^a-zA-Z0-9]+', '_', romaji.strip()).strip('_') or hashlib.md5(romaji.encode()).hexdigest()[:12]
            mp3_name = f"cat__{safe}__{str(wid)[:8]}.mp3"

            with tempfile.NamedTemporaryFile(suffix=".mp3", delete=False) as tmp:
                tmp_path = tmp.name

            communicate = Communicate(reading or word, "ja-JP-NanamiNeural")
            await communicate.save(tmp_path)

            with open(tmp_path, "rb") as f:
                supabase.storage.from_(VOCAB_AUDIO_BUCKET).upload(
                    path=mp3_name,
                    file=f.read(),
                    file_options={"content-type": "audio/mpeg", "upsert": "true"},
                )

            mp3_url = supabase.storage.from_(VOCAB_AUDIO_BUCKET).get_public_url(mp3_name)
            os.unlink(tmp_path)
            return mp3_url
        except Exception as e:
            log.warning(f"  MP3 error: {e}")
            return None


def set_mp3(conn, wid, mp3_url):
    """更新 DB mp3 字段"""
    try:
        with conn.cursor() as cur:
            cur.execute("UPDATE vocab_library SET mp3 = %s WHERE id = %s", (mp3_url, wid))
        return True
    except Exception:
        return False


async def main_async(args):
    conn = get_conn()
    conn.autocommit = True

    # Step 1: Update all fields
    log.info(f"── 更新 100 条技能词条所有字段 ──")
    updated = 0
    with conn.cursor() as cur:
        for data in ABILITIES:
            word, reading, pos, freq, pitch, meaning, examples, insight, source = data
            heatmap = {"游戏语音": 90, "Discord群": 82, "Twitch直播弹幕": 75, "新手教学": 60}
            if update_ability(conn, cur, args.dry_run, word, reading, pos, freq, pitch,
                              meaning, examples, insight, source, heatmap):
                updated += 1

    log.info(f"更新完成: {updated}/{len(ABILITIES)} 条")

    if args.dry_run:
        log.info("\n=== DRY RUN === 未实际修改")
        conn.close()
        return

    # Step 2: Generate MP3
    if not args.skip_mp3:
        log.info(f"── 生成 MP3 音频 ──")
        sem = asyncio.Semaphore(3)
        mp3_count = 0

        for data in ABILITIES:
            word, reading = data[0], data[1]
            with conn.cursor() as cur:
                cur.execute("SELECT id FROM vocab_library WHERE word = %s AND tags @> ARRAY['gaming_valorant','gaming_val_agents']::text[]", (word,))
                row = cur.fetchone()
                if not row:
                    continue
                wid = row[0]

            mp3_url = await gen_mp3(sem, wid, word, reading)
            if mp3_url and set_mp3(conn, wid, mp3_url):
                mp3_count += 1
                if mp3_count % 20 == 0:
                    log.info(f"  MP3 进度: {mp3_count}/100")

        log.info(f"MP3 生成完成: {mp3_count}/100 条")

    conn.close()
    log.info("\n全部完成！")


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--dry-run", action="store_true", help="仅预览不执行")
    parser.add_argument("--skip-mp3", action="store_true", help="跳过 MP3 生成")
    args = parser.parse_args()

    asyncio.run(main_async(args))


if __name__ == "__main__":
    main()
